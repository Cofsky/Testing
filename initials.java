
/**
 * Displays first and last initial in a vertically descending order.
 *
 * @author Dylan Cofsky
 * @version September 3rd 2020
 */
public class initials
{
    public static void main (String [] args)
    {
        displayFirstInitial();
        displayLastInitial();
    }

    public static void displayFirstInitial ()
    {
        System.out.println(" D D D ");
        System.out.println(" D     D");
        System.out.println(" D       D");
        System.out.println(" D       D");
        System.out.println(" D       D");
        System.out.println(" D       D");
        System.out.println(" D     D");
        System.out.println(" D D D");
    }

    public static void displayLastInitial ()
    {
        System.out.println("     C C C C");
        System.out.println("   C");
        System.out.println(" C");
        System.out.println(" C");
        System.out.println(" C");
        System.out.println(" C");
        System.out.println("   C");
        System.out.println("     C C C C");
    }
}
